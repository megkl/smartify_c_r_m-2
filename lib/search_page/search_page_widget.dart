                                              .map(
                                                (record) => TextSearchItem(
                                                    record, [
                                                  record.displayName!,
                                                  record.email!
                                                ]),
                                              )
                                              .toList(),
                                        )
                                            .search(textController!.text)
                                            .map((r) => r.object)
